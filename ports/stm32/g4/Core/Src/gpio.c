void MX_GPIO_InitRecursive(void)
{
	if (!initialized) {
		MX_GPIO_Init();
		initialized = true;
	}
}

void MX_GPIO_RegisterCallback(uint16_t GPIO_Pin, void (*cb)(void))
{
	int pin_index = __builtin_ctz(GPIO_Pin);

	assert(is_power2(GPIO_Pin));
	assert(pin_index < sizeof(callbacks) / sizeof(callbacks[0]));

	callbacks[pin_index] = cb;
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	int pin_index = __builtin_ctz(GPIO_Pin);

	if (callbacks[pin_index]) {
		(*callbacks[pin_index])();
	}
}
